'''
ограничение по времени на тест 1 секунда
ограничение по памяти на тест 1024 мегабайта
Дан массив из 𝑛 чисел 𝑎𝑖. Скажем, что отрезок этого массива 𝑎[𝑙..𝑟] (1≤𝑙≤𝑟≤𝑛) хороший, если сумма чисел на этом отрезке
не меньше 𝑠. Ваша задача — найти число различных хороших отрезков.

Входные данные
Первая строка содержит целые числа 𝑛 и 𝑠 (1≤𝑛≤105, 1≤𝑠≤1018). Вторая строка содержит числа 𝑎𝑖 (1≤𝑎𝑖≤109).

Выходные данные
Выведите одно число — число хороших отрезков.

Пример
входные данные
7 20
2 6 4 3 6 8 9
выходные данные
9
'''

n, s = map(int, input().split())
a = list(map(int, input().split()))

i, j = 0, 0
max_sum = 0
ans = 0

while j < n:
    max_sum += a[j]
    if max_sum < s:
        j += 1
    else:
        ans += n - j
        max_sum -= a[i]
        max_sum -= a[j]
        i += 1
print(ans)

