n, s = map(int, input().split())
a = list(map(int, input().split()))

j = 0
ans = 0
sum_otr = 0
for i in range(n):
    while j < n and sum_otr + a[j] <= s:
        sum_otr += a[j]
        j += 1
    #[i, j]
    ans += j - i
    sum_otr -= a[i]
print(ans)