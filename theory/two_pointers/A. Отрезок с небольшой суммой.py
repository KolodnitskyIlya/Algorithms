a = list(map(int, input().split()))

i = 0
j = 0

ans = 0
max_ans = 0
sum = 0
while j < n:
    sum += a[j]
    if sum <= s:
        j += 1
        ans += 1
    else:
        sum -= a[i]
        sum -= a[j]
        i += 1
        ans -= 1
    if ans > max_ans:
        max_ans = ans
