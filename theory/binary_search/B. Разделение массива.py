def ok(mid, a, k):
    cnt_otr = 0
    sum_otr = 0
    for i in range(len(a)):

        if a[i] > mid:
            return False

        if sum_otr + a[i] > mid:
            cnt_otr += 1
            sum_otr = a[i]
        else:
            sum_otr += a[i]
    if sum_otr > 0:
        cnt_otr += 1
    return cnt_otr <= k


n, k = map(int, input().split())
a = list(map(int, input().split()))

l = 0
r = 10**14
ans = 0
while l <= r:
    mid = (l + r) // 2
    if ok(mid, a, k):
        r = mid - 1
        ans = mid
    else:
        l = mid + 1

print(ans)