def ok(mid, a, k):
    x = 1
    last_id = 0
    for i in range(1, len(a)):
        if a[i] - a[last_id] >= mid:
            x += 1
            last_id = i
    return x >= k

n, k = map(int, input().split())
a = list(map(int, input().split()))

l = 0
r = 10**9
ans = 0
while l <= r:
    mid = (l + r) // 2
    if ok(mid, a, k):
        l = mid + 1
        ans = mid
    else:
        r = mid - 1
print(ans)