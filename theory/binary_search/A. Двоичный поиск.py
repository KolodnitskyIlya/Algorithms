n, k = map(int, input().split())
nums = list(map(int, input().split()))
requests = list(map(int, input().split()))

def ok(nums, mid, x):
    return nums[mid] <= x

for i in range(0, len(requests)):
    l = 0
    r = n - 1
    x = requests[i]
    ans = 'NO'
    while l <= r:
        mid = (l + r) // 2
        if ok(nums, mid, x):
            if nums[mid] == x:
                ans = "YES"
                break
            l = mid + 1
        else:
            r = mid - 1

    print(ans)
