def ok(mid, w, h):
    return (mid // w) * (mid // h) >= n

w, h, n = map(int, input().split())

l = 0
r = 10**19
ans = 0
while l <= r:
    mid = (l + r) // 2
    if ok(mid, w, h):
        r = mid - 1
        ans = mid
