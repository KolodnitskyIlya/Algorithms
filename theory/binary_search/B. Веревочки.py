'''
[a0 / x] + [a1 / x] + ... + [an / x] >= k
'''

def ok(mid, a, k):
    sum = 0
    for i in range(len(a)):
        sum += int(a[i] / mid)
    return sum >= k

n, k = map(int, input().split())
a = [0] * n
for i in range(n):
    a[i] = int(input())

l = 0
r = 10**18
ans = 0

for i in range(100):
    mid = (l + r) / 2
    if ok(mid, a, k):
        l = mid
        ans = mid
    else:
        r = mid
print(l)

